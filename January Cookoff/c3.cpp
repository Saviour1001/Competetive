#include<bits/stdc++.h>
using namespace::std;
bool isPrime(int n) 
{ 
    // Corner cases 
    if (n <= 1) 
        return false; 
    if (n <= 3) 
        return true; 
  
    // This is checked so that we can skip 
    // middle five numbers in below loop 
    if (n % 2 == 0 || n % 3 == 0) 
        return false; 
  
    for (int i = 5; i * i <= n; i = i + 6) 
        if (n % i == 0 || n % (i + 2) == 0) 
            return false; 
  
    return true; 
} 
int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    int t;
    cin>>t;
    while(t--)
    {
        int n;
        cin>>n;
        int counter=0;
        for(int i=0;i<=n-2;i++)
        {
            if(isPrime(i) && isPrime(i+2))
            {
                counter+=1;
            }
        }
        cout<<counter<<"\n";
    }
    return 0;
}