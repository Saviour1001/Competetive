int LCSubStr(string X,string Y)
{
    int m=X.length();
    int n=Y.length();
 
    int LCSuff[m + 1][n + 1];
    int result = 0; 
 
    for (int i = 0; i <= m; i++)
    {
        for (int j = 0; j <= n; j++)
        {
            if (i == 0 || j == 0)
                LCSuff[i][j] = 0;
 
            else if (X[i - 1] == Y[j - 1]) {
                LCSuff[i][j] = LCSuff[i - 1][j - 1] + 1;
                result = max(result, LCSuff[i][j]);
            }
            else
                LCSuff[i][j] = 0;
        }
    }
    return result;
}

int LC(string a, string b)
{
    int result = 0;
    int m = a.length();
    int n = b.length();
    int dp[m+1][n+1];
    for (int i = 0; i <= m; i++)
    {
        for (int j = 0; j <= n; j++)
        {
            if (i == 0 || j == 0)
            {
                dp[i][j] = 0;
            }
            else if (a[i - 1] == b[j - 1])
            {
                dp[i][j]=dp[i-1][j-1]+1;
                result=max(result,dp[i][j]);
            }
            else
            {
                dp[i][j]=0;
            }
        }
    }
    return result;
}

